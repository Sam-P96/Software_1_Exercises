a = int(input("Please enter an integer: "))
b = int(input("Please enter another integer: "))
c = int(input("Please enter another integer: "))

sum_abc = a + b + c
product_abc = a * b * c
average_of_abc = (a + b + c) / 3

print(f"The sum of {a}, {b}, and {c} is {sum_abc}.")
print(f"The product of {a}, {b}, and {c} is {product_abc}")
print(f"The average of {a}, {b}, and {c} is {average_of_abc}")